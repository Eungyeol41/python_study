# 사칙 연산자: +, -, *, /
print("5 + 7 = ", 5 + 7)
print("5 - 7 = ", 5 - 7)
print("5 * 7 = ", 5 * 7)
print("5 / 7 = ", 5 / 7)

# 정수 나누기 연산자: //
print("3 / 2 = ", 3 / 2) # 나누기 연산 그대로 사용
print("3 // 2 = ", 3 // 2) # 나누기 연산 사용 후 소수점 버림

# 나머지 연산자: %
print("5 % 2 = ", 5 % 2)

# 제곱 연산자: **
print("2 ** 1 = ", 2 ** 1)
print("2 ** 2 = ", 2 ** 2)
print("2 ** 3 = ", 2 ** 3)
print("2 ** 4 = ", 2 ** 4)
